# While loops are loops that run while the condition set is TRUE

# Syntax is while (condition)

x = 0

# This loop will carry on running until x is equal to 5, x is incremented by 1 each iteration of the loop
# So this is looping until x is less than or equal to 5
while x <= 5:
    print(f"it's working -> {x}")
    x += 1
